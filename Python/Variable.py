# Variables in Python

# Variables are used to store data values. A variable is a name given to a memory location in a program. Variables are created when you assign a value to it. Variables do not need to be declared with any particular type and can even change type after they have been set.

# Variable names are case-sensitive.


integer_variable = 10
float_variable = 10.5
string_variable = "Hello World"
boolean_variable = True

print(
    f"This is integer = {integer_variable}, float = {float_variable}, string = '{string_variable}', boolean = {boolean_variable}"
)
